// Loops
let myNumber=0;
while(myNumber>50){
    myNumber++;
    console.log(myNumber);
}

// do-while loop
// condition in do will be executed atleast once    
do{
    console.log(myNumber);
}
while(myNumber<50);

// for loop
let userName="Dave";
for(let i=0;i<userName.length;i++){
    console.log(userName[i]);
}

